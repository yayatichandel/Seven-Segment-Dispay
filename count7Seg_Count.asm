RESET10:MOV R2,#0BH
TENS:CLR P3.4
SETB P3.3
MOV A,#3BH
SUBB A,R2
MOV R0,A
MOV P1,@R0
CALL DELAY
CALL DELAY
DJNZ R2,RESET1
JMP RESET10

RESET1:MOV R1,#0AH
CLR P3.3
CLR P3.4

ONES:MOV A,#3AH
SUBB A,R1
MOV R0,A
MOV P1,@R0
CALL DELAY
CALL DELAY
DJNZ R1,ONES
JMP TENS

DELAY: MOV R3,#0FFH
HERE:DJNZ R0,HERE
RET
